// push function array

let fruits = ["Apple", "Banana"];

fruits.push("Mango");

console.log(fruits);



// pop function array

let colors = ["Red", "Green", "Blue"];

colors.pop();

console.log(colors);


//shift function array

let cities = ["Delhi", "Mumbai", "Pune"];

cities.shift();

console.log(cities);



//unshift function array

let numbers = [20, 30, 40];

numbers.unshift(10);

console.log(numbers);

// splice function array


let marks = [50, 60, 70, 80];

marks.splice(1, 2);

console.log(marks);
